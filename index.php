document.addEventListener("DOMContentLoaded", function () {

    /* ===========================
       Navbar Scroll Effect
    =========================== */

    const navbar = document.querySelector(".navbar");

    function navbarScroll(){

        if(window.scrollY > 80){

            navbar.classList.add("scrolled");

        }else{

            navbar.classList.remove("scrolled");

        }

    }

    navbarScroll();

    window.addEventListener("scroll", navbarScroll);


    /* ===========================
       Hero Carousel
    =========================== */

    const heroSlider = document.querySelector("#mainSlider");

    if(heroSlider){

        new bootstrap.Carousel(heroSlider,{

            interval:5000,

            ride:"carousel",

            pause:false,

            touch:true,

            wrap:true

        });

    }


    /* ===========================
       Fade Up Animation
    =========================== */

    const observer = new IntersectionObserver(function(entries){

        entries.forEach(function(entry){

            if(entry.isIntersecting){

                entry.target.classList.add("show");

            }

        });

    },{

        threshold:0.15

    });

    document.querySelectorAll(".business-card").forEach(function(el){

        observer.observe(el);

    });

    document.querySelectorAll(".product-card").forEach(function(el){

        observer.observe(el);

    });

    document.querySelectorAll(".history-box").forEach(function(el){

        observer.observe(el);

    });

    document.querySelectorAll(".news-card").forEach(function(el){

        observer.observe(el);

    });

});

</script>

